    private void jbtnMultiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnMultiActionPerformed
        if (!entrada.isEmpty()) {
        double numeroActual = Double.parseDouble(entrada);
        if (!operador.isEmpty()) {
            if (operador.equals("+")) numero1 += numeroActual;
            else if (operador.equals("-")) numero1 -= numeroActual;
            else if (operador.equals("*")) numero1 *= numeroActual;
            else if (operador.equals("/")) {
                if (numeroActual != 0) numero1 /= numeroActual;
                else jtxCnatidad.setText("Error");
            }
            jtxCnatidad.setText(String.valueOf(numero1));
        } else {
            numero1 = numeroActual;
        }
        entrada = "";     
        operador = "*";    
    }
    }//GEN-LAST:event_jbtnMultiActionPerformed

