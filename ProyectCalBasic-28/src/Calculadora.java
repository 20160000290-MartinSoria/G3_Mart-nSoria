    private void jbtnPorcentajeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnPorcentajeActionPerformed
        if (!entrada.isEmpty()) {
        double valor = Double.parseDouble(entrada);
        valor = valor / 100;
        entrada = String.valueOf(valor);
        jtxCnatidad.setText(entrada);
        }
    }//GEN-LAST:event_jbtnPorcentajeActionPerformed

    private void jbtnCActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnCActionPerformed
        String texto = jtxCnatidad.getText();
    if (texto.length() > 1) {
        texto = texto.substring(0, texto.length() - 1);
        jtxCnatidad.setText(texto);
        entrada = texto; 
    } else {
        jtxCnatidad.setText("0");
        entrada = "";
    }
    }//GEN-LAST:event_jbtnCActionPerformed

    private void jbtnRaizActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnRaizActionPerformed
    double valor = Double.parseDouble(jtxCnatidad.getText());
    if (valor >= 0) {
        valor = Math.sqrt(valor);
        jtxCnatidad.setText(String.valueOf(valor));
        entrada = String.valueOf(valor);
    } else {
        jtxCnatidad.setText("Error");
        entrada = "";
    }
    }//GEN-LAST:event_jbtnRaizActionPerformed

    private void jbtnPuntoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnPuntoActionPerformed
        if (entrada.isEmpty()) {
            entrada = "0.";
        } else {
            // 3. Si ya tiene números, simplemente concatenamos el punto 
            entrada = entrada + ".";
        }
        
        // 4. Actualizamos la pantalla [cite: 108]
        jtxCnatidad.setText(entrada);
    }//GEN-LAST:event_jbtnPuntoActionPerformed

